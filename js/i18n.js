(function () {
  function getLang() {
    var lang = localStorage.getItem("zuzi-lang");
    return lang && window.ZUZI_I18N[lang] ? lang : "en";
  }

  function resolve(obj, path) {
    return path.split(".").reduce(function (o, k) {
      return o == null ? undefined : o[k];
    }, obj);
  }

  function t(key) {
    var lang = getLang();
    var dict = window.ZUZI_I18N[lang] || {};
    return resolve(dict, key) != null ? resolve(dict, key)
      : (resolve(window.ZUZI_I18N.en, key) != null ? resolve(window.ZUZI_I18N.en, key) : key);
  }

  window.zuziLang = getLang;
  window.t = t;

  window.zuziTour = function (tour) {
    if (!tour) return tour;
    var lang = getLang();
    var tr = ((window.ZUZI_I18N[lang] || {}).tourData || {})[tour.id];
    if (!tr) return tour;
    var or = function (a, b) { return b != null ? b : a; };
    return {
      id: tour.id, icon: tour.icon, image: tour.image,
      rating: tour.rating, reviews: tour.reviews,
      name: or(tour.name, tr.name),
      region: or(tour.region, tr.region),
      tagline: or(tour.tagline, tr.tagline),
      bestFor: or(tour.bestFor, tr.bestFor),
      duration: or(tour.duration, tr.duration),
      durationShort: or(tour.durationShort, tr.durationShort),
      cardDesc: or(tour.cardDesc, tr.cardDesc),
      longDesc: or(tour.longDesc, tr.longDesc),
      highlights: (tour.highlights || []).map(function (h, i) {
        return { icon: h.icon, title: or(h.title, tr.highlights && tr.highlights[i]) };
      }),
      gallery: (tour.gallery || []).map(function (g, i) {
        return { src: g.src, caption: or(g.caption, tr.gallery && tr.gallery[i]) };
      }),
      itinerary: (tour.itinerary || []).map(function (s, i) {
        var ts = tr.itinerary && tr.itinerary[i];
        return {
          time: s.time,
          title: or(s.title, ts && ts.title),
          text: or(s.text, ts && ts.text),
        };
      }),
      included: tr.included || tour.included,
      excluded: tr.excluded || tour.excluded,
    };
  };

  function applyTranslations(root) {
    root = root || document;
    var lang = getLang();
    var meta = window.ZUZI_LANGS.find(function (l) { return l.code === lang; }) || window.ZUZI_LANGS[0];
    document.documentElement.lang = lang;
    document.documentElement.dir = meta.dir;

    root.querySelectorAll("[data-i18n]").forEach(function (el) {
      el.textContent = t(el.getAttribute("data-i18n"));
    });
    root.querySelectorAll("[data-i18n-html]").forEach(function (el) {
      el.innerHTML = t(el.getAttribute("data-i18n-html"));
    });
    root.querySelectorAll("[data-i18n-aria]").forEach(function (el) {
      el.setAttribute("aria-label", t(el.getAttribute("data-i18n-aria")));
    });
    root.querySelectorAll(".lang-current").forEach(function (el) {
      el.textContent = meta.flag;
    });
    root.querySelectorAll("[data-lang]").forEach(function (b) {
      b.classList.toggle("active", b.getAttribute("data-lang") === lang);
    });
  }
  window.zuziApplyTranslations = applyTranslations;

  window.zuziSetLang = function (lang) {
    if (!window.ZUZI_I18N[lang]) lang = "en";
    localStorage.setItem("zuzi-lang", lang);
    if (window.zuziRender) window.zuziRender();
    else applyTranslations();
  };

  function initLangSwitch() {
    document.addEventListener("click", function (e) {
      var menu = document.querySelector(".lang-menu");
      var btn = e.target.closest(".lang-btn");
      if (btn && menu) {
        menu.classList.toggle("open");
        return;
      }
      var item = e.target.closest("[data-lang]");
      if (item) {
        window.zuziSetLang(item.getAttribute("data-lang"));
        if (menu) menu.classList.remove("open");
        return;
      }
      if (menu && !e.target.closest(".lang-switch")) {
        menu.classList.remove("open");
      }
    });
  }
  window.zuziInitLangSwitch = initLangSwitch;
})();
